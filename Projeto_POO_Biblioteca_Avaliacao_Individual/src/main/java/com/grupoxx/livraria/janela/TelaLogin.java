    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       this.user = this.jTextField1.getText();
       this.password = this.jPasswordField1.getText();
       
       if(!this.user.equalsIgnoreCase("") && !this.password.equalsIgnoreCase("")){
            if(!Sistema.fazerLogin(user, password))
                JOptionPane.showMessageDialog(null, "Senha ou Login incorreto");
            else if(this.user.equalsIgnoreCase("admin") && this.password.equalsIgnoreCase("admin")){
                new TelaDoAdmin().setVisible(true);
                dispose();

             }
            else{
                new TelaDoUsuario().setVisible(true);
                dispose();
             }
       }
       else
           JOptionPane.showMessageDialog(null, "Entre com informações validas");
   
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        this.user = this.jTextField1.getText();
        this.password = this.jPasswordField1.getText();
        
        if(!this.user.equalsIgnoreCase("") && !this.password.equalsIgnoreCase("")){
            try {
                if(!Sistema.cadastraUsuario(user, password))
                    JOptionPane.showMessageDialog(null, "Usuario já cadastrado");
                else if(this.user.equalsIgnoreCase("admin") && this.password.equalsIgnoreCase("admin")){
                    new TelaDoAdmin().setVisible(true);
                    dispose();
                    
                }
                else{
                    JOptionPane.showMessageDialog(null, this.user+" Cadastrado com sucesso");
                    new TelaDoUsuario().setVisible(true);
                    dispose();
                }
            } catch (UsuarioCriacaoException ex) {
                ex.getMessage();
            } catch (IOException ex) {
                System.out.println("Usuario não existe");
            }
        }
        else
            JOptionPane.showMessageDialog(null, "Entre com informações validas");
    }//GEN-LAST:event_jButton2ActionPerformed

